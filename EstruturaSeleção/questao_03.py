
''' 3. Faça um Programa que verifique se uma letra digitada é
"F" ou "M". Conforme a letra escrever: F - Feminino, M - Masculino.'''

letra = input('Digite F - Feminino , M - Masculino: ')

if letra == 'F':
    print('A letra digita é F - Feminino')
elif letra == 'M':
    print('A letra digitada é M - Masculino')
else:
    print('Letra inválida')